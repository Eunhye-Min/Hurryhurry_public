package com.toilet.vo;

public class LikeVO {
	private String like_idx,
					review_idx,
					userID;

	public String getLike_idx() {
		return like_idx;
	}

	public void setLike_idx(String like_idx) {
		this.like_idx = like_idx;
	}

	public String getReview_idx() {
		return review_idx;
	}

	public void setReview_idx(String review_idx) {
		this.review_idx = review_idx;
	}

	public String getUserID() {
		return userID;
	}

	public void setUserID(String userID) {
		this.userID = userID;
	}
	
	
}
